INNER JOIN Session as s ON p.Player_Id = s.Player_Id
ORDER BY CASE WHEN s.Stop_Epoch IS NULL THEN 0 ELSE 1 END,
         s.Start_Epoch ASC,
         c.Player_Category_Id ASC,
         IFNULL(p.NickName,p.Name) ASC;
